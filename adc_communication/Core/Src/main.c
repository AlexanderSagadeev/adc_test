/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  MX_SPI1_Init();
  /* USER CODE BEGIN 2 */
  HAL_ADCEx_Calibration_Start(&hadc1);
  HAL_Delay(10);
  WIZNET_Init();
  HAL_TIM_OC_Start_IT(&htim4, TIM_CHANNEL_4);
  HAL_ADC_Start_IT(&hadc1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  /* buffer for transfer data */
  uint16_t buf_tx[BUF_TX_LEN];

  /* buffer for receive data */
  uint8_t buf_rx[BUF_RX_LEN];

  /* IP address and port of computer and WIZNET w5500 */
  uint8_t addr_pc[4] = {192, 168, 0, 178};
  uint16_t port_pc = 4023;
  //uint8_t addr_wz[4] = {192, 168, 0, 177};
  uint16_t port_wz = 1000;
  GPIOC->BSRR = GPIO_BSRR_BS12;
  RX_data recv_data;
  Frame frame;

  /* create UDP socket and connect */
  socket(UDP_SOCKET, Sn_MR_UDP, port_wz, 0);
  connect(UDP_SOCKET, addr_pc, port_pc);

  while (1)
  {
	  /* receive data: channel ADC, command, data;
	   * configure parameters if required */
	   // 00 01 04 00 00 10 - channel, samples, timer
	  if(recvfrom(UDP_SOCKET, buf_rx, (uint16_t)BUF_RX_LEN, addr_pc, &port_pc) > 0){
		  fill_data(&recv_data, buf_rx, &frame);
		  set_period(&htim4, TIM_CHANNEL_4, recv_data.timer_period);
	  }

	  /* circular send data for one or whole ADC channels */
	  for(int j = recv_data.adc_channel; j < recv_data.send_num; j++){
		  /* temporary stop timer and create frame for transfer data */
		  HAL_TIM_OC_Stop_IT(&htim4, TIM_CHANNEL_4);
		  frame_creation(buf_tx, psamp_buff[j], (uint16_t)BUF_TX_LEN, (uint16_t)BUF_LEN, samp_pos, &recv_data, &frame);
		  HAL_TIM_OC_Start_IT(&htim4, TIM_CHANNEL_4);

		  /* transfer data */
		  for(int i = 0, nbyte = 0; nbyte < frame.total_data && i < frame.num_of_frames; i++){
			  nbyte += sendto(UDP_SOCKET, frame.buff_ptr[i], frame.frame_len, addr_pc, port_pc);
		  }
		  clear_data(&frame, &recv_data, j + 1);
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  disconnect(UDP_SOCKET);
  close(UDP_SOCKET);
  /* USER CODE END 3 */
}


void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim)
{
	/* toggle pin for timer work visualization */
	HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_12);

	/* if counter more than length of buffer, start over */
	if(samp_pos >= (uint16_t)BUF_LEN)
		samp_pos = 0;

	/* Every timer interrupt write value from ADC buffer
	 * to buffer with required sampling.
	 * Values from ADC channels sequentially arranged at analog buffer. */
	for(int i = 0; i < CH_NUM; i ++)
		psamp_buff[i][samp_pos] = analog_buff[i];
	samp_pos++;
}

